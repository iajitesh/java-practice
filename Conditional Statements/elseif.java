public class elseif {
    public static void main(String[] args) {
        int age = 22;
        if (age < 22) {
            System.out.println("Chhota bhai!");
        } else if (age == 22) {
            System.out.println("Bhai bhai!");
        } else {
            System.out.println("Bada bhai!");
        }
    }
}
